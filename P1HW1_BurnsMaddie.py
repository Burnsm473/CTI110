#Calculating Exponents

x = float(input("Enter an integer as the base value:"))
y = float(input("Enter an integer as the exponent:"))
z = x ** y

print(f"{x} raised to the power of {y} is {z}")

# Addition and subtraction

a = float(input("Enter a starting integer:"))
b = float(input("Enter an integer to add:"))
c = float(input("Enter an integer to subtract:"))
d = a + b - c

print(f"{a} + {b} - {c} is equal to {d}")

